/**
 * Definition for singly-linked list provided by leetcode to support the solution to 'Add Two Numbers', problem 2.
 *
 * @author leetcode.com
 */


public class ListNode {
    int val;
    ListNode next;
    ListNode() {}
    ListNode(int val) { this.val = val; }
    ListNode(int val, ListNode next) { this.val = val; this.next = next; }
}
